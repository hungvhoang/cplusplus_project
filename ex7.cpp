#include <iostream>
int main() {
	int a=5, b=7;
	double c= 3.7, d= 8.0;
	std::cout << a <<"+"<<b <<" = "<< a+b<<"\n";
	std::cout << c <<"+"<<d <<" = "<< c+d<<"\n";
	std::cout << a <<"+"<<d <<" = "<< a+d<<"\n";
	std::cout << a <<"-"<<b <<" = "<< a-b<<"\n";
	std::cout << c <<"-"<<d <<" = "<< c-d<<"\n";
	std::cout << a <<"*"<<b <<" = "<< a*b<<"\n";
	std::cout << c <<"*"<<d <<" = "<< c*d<<"\n";
	std::cout << a <<"*"<<d <<" = "<< a*d<<"\n";
	std::cout << a <<"/"<<b <<" = "<< a/b<<"\n";
	std::cout << c <<"/"<<d <<" = "<< c/d<<"\n";
	std::cout << a <<"/"<<d <<" = "<< a/d<<"\n";
	
}
