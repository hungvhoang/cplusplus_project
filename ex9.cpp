#include <iostream>
int main() {
	int a=57;
	std::cout <<"The number is: "<<a<<std::endl;
	a++;
	std::cout<< "After post increment by 1 the number is: "<<a<<std::endl;
	++a;
	std::cout << "After pre increment by 1 the number is: "<<a<<std::endl;
	a=a+1;
	std::cout << "After increasing by 1 the number is: "<<a<<std::endl;

}
